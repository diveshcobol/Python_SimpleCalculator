# Simple Calculator Project in Python

print("Simple Python Calculator")
print("------------------------")

a = float(input("Enter first number: "))
b = float(input("Enter second number: "))

print("\nResults:")
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Modulus:", a % b)
print("Power:", a ** b)
print("Division:", a / b)
print("Floor Division:", a // b)